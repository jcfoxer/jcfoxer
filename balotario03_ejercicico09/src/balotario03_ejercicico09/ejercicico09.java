    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        double consumo_inic ,desca=0,descb=0,descc=0,descd=0,importe_total,descuento_total=0;
        
         consumo_inic=Integer.parseInt(txt1.getText());
        
        
        
        if (consumo_inic> 200){
    desca = 0.30 *consumo_inic;}
   else
         {desca = 0;}
 
     if (consumo_inic> 160){
    descb = 0.20 *consumo_inic;}
   else
         {desca = 0;}
        
     if (consumo_inic> 130){
    descc = 0.15 *consumo_inic;}
   else
         {desca = 0;}
        
     if (consumo_inic< 130){
    descd = 0.10 *consumo_inic;}
   else
         {desca = 0;}
        
        descuento_total=desca+descb+descc+descd;
     importe_total=consumo_inic-descuento_total;
       
     
   txt1.setText(+ consumo_inic + "\n"); 
   txt2.setText(+ desca + "\n");
   txt3.setText(+ descb + "\n");  
   txt4.setText(+ descc + "\n");   
   txt6.setText(+ descd + "\n");  
   txt5.setText(+ importe_total + "\n");  
     
     
    }//GEN-LAST:event_jButton1ActionPerformed

