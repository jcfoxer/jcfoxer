    private void txtdineroseforaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtdineroseforaActionPerformed
     
        //decklaracion de varibles siempre en event action event 
        double dinedeb,dineraq,dinesef,capital,porcdeb,porcraq,porsef;
        //entrada de datos 
        dinedeb=Double.parseDouble(txtdinerodebora.getText());
        dineraq=Double.parseDouble(txtdineroraquel.getText());
        dinesef=Double.parseDouble(txtdinerosefora.getText());
        
        // determina la  cantidad formado 
        
        capital=dinedeb+dineraq+dinesef;
        
        //determina los procentajes 
        
        porcdeb=dinedeb*100/capital;
         porcraq=dineraq*100/capital;
          porsef=dinesef*100/capital;
          
    //salida de resultados 
 txts.setText("capital           :"+capital+"\n");    
txts.append("porcentaje de debora:" +porcdeb +"\n");        
txts.append("porcentaje de raquel:" +porcraq +"\n");          
txts.append("porcentaje de sefora:" +porsef +"\n");          
        
    }//GEN-LAST:event_txtdineroseforaActionPerformed
